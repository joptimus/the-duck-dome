function normalizeAgent(agent) {
  const normalized = String(agent || "").toLowerCase();
  if (BRAND_PATHS[normalized] || AGENT_COLORS[normalized]) {
    return normalized;
  }
  const unsuffixed = normalized.replace(/-\d+$/, "");
  return unsuffixed;
}

export function AgentLogo({ agent, size = 20 }) {
  const normalizedAgent = normalizeAgent(agent);
  const color = AGENT_COLORS[normalizedAgent] || "var(--text-muted)";
  const legacyBrand = BRAND_PATHS[normalizedAgent];

  if (legacyBrand) {
    const paths = legacyBrand.paths || [legacyBrand.path];
    return (
      <svg width={size} height={size} viewBox={legacyBrand.viewBox} fill="none">
        {paths.map((path, index) => (
          <path key={`${normalizedAgent}-${index}`} d={path} fill={legacyBrand.fill || color} />
        ))}
      </svg>
    );
  }

  if (normalizedAgent === "minimax") {
    return (
      <svg width={size} height={size} viewBox="0 0 24 24" fill="none">
        <path d="M2 16C5 10 8 18 12 12C16 6 19 14 22 8" stroke={color} strokeWidth="2" strokeLinecap="round" />
        <circle cx="12" cy="12" r="2" fill={color} />
      </svg>
    );
  }

  return (
    <svg width={size} height={size} viewBox="0 0 24 24">
      <text
        x="12"
        y="16"
        textAnchor="middle"
        fill={color}
        fontSize="14"
        fontWeight="700"
        fontFamily="Orbitron"
      >
        {agent?.[0]?.toUpperCase() || "?"}
      </text>
    </svg>
  );
}
